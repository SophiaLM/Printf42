void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, str[i], 1);
		i++;
	}
}

int	main()
{
	char	*p = "HOLA";
	ft_putstr(p);
	return (0);
}
