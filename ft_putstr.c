#include "ft_printf.h"

void	ft_putstr(char *str, int *count)
{
	int	i;

	i = 0;
	if (!str)
		ft_putstr("(null)", count);
	else
	{
		while (str[i] != '\0')
		{
			*count += write(1, &str[i++], 1);
		}
	}
}
